    protected void datatablepelanggan() {
        Object[] Baris = {"Id Pelanggan", "Nama Pelanggan", "Alamat Pelanggan", "No telp Pelanggan"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelpelanggan.setModel(tabmode);
        String sql = "select * from datapelanggan";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                String a = hasil.getString("id_pelanggan");
                String b = hasil.getString("nama_pelanggan");
                String c = hasil.getString("alamat_pelanggan");
                String d = hasil.getString("notelp_pelanggan");
                String[] data = {a, b, c, d};
                tabmode.addRow(data);
            }
        } catch (Exception e) {

        }
    }
